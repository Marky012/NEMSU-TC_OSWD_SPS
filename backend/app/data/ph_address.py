def get_regions():
    return [{"code": k, "name": v} for k, v in REGIONS.items()]

def get_provinces(region_code=None):
    result = [{"code": k, "name": v[0], "region_code": v[1]} for k, v in PROVINCES.items()]
    if region_code:
        result = [p for p in result if p["region_code"] == region_code]
    return sorted(result, key=lambda x: x["name"])

def get_cities(province_code=None):
    result = []
    for prov_code, city_list in CITIES.items():
        for city_code, city_name in city_list:
            entry = {"code": city_code, "name": city_name, "province_code": prov_code}
            result.append(entry)
    if province_code:
        result = [c for c in result if c["province_code"] == province_code]
    return sorted(result, key=lambda x: x["name"])

def get_barangays(city_code=None):
    result = []
    for cc, brgys in BARANGAYS.items():
        for b in brgys:
            entry = {"name": b, "city_code": cc}
            result.append(entry)
    if city_code:
        result = [b for b in result if b["city_code"] == city_code]
    return sorted(result, key=lambda x: x["name"])
